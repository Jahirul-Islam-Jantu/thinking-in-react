function getDay(dateStr){
    const date = new Date(dateStr).getDay();
    const days = [
        "Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"
    ]
    return days[date];
}



function formatDate(dateStr){
    const date = new Date(dateStr)
    return `${date.getFullYear()}-${date.getMonth() + 1}-${date.getDate()}`
}

const TagListItem = ({tag})=> {
    return <li key={tag.id}><small>{tag.icon} - {tag.text}</small></li>
}
const CommentListItem = ({comment})=> {
    return <div key={comment.id}><h3>{comment.user.name} </h3> <p>{comment.text}</p></div>
}
const TaskListItem = ({task})=> {
    return (<li key={task.id}><h3>{task.title}</h3> <p><small>{task.status}</small></p> <p>{task.text}</p></li>)
}
const TaskCard = ({task})=> {
    return (<div className="box-card">
        <h1 className="title">{getDay(task.createdAt)}, {formatDate(task.createdAt)}</h1>
        <h3 className="sub-title">{task.subtitle}</h3>
        <ul className="tag-ul">
            {task.tags.map((tag) => <TagListItem key={tag.id} tag={tag}/>)}
        </ul>
        <div className="line"/>
        <p>Note's Linked to people</p>
        <div className="comments">
            {task.comments.map((comment) => <CommentListItem key={comment.id} comment={comment}/>)}
        </div>
        <ul className="tasks">
            {task.tasks.map((task) => <TaskListItem key={task.id} task={task}/>)}
        </ul>
    </div>)
}

const ToDoApp = () => {
    return (
        <div className="task-elements">

            {tasks.map(task => <TaskCard task={task} key={task.id} />)}
        </div>
    );
};

export default ToDoApp;